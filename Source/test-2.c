#include <stdio.h>

int main()
{
    int x, y, hap, cha, gop, mok;
    x = 20;
    y = 10;


    hap = x + y;
    cha = x - y;
    gop = x * y;
    mok = x / y;
    printf("두수의 합: %d\n", hap);
    printf("두수의 차: %d\n", cha);
    printf("두수의 곱: %d\n", gop);
    printf("두수의 몫: %d", mok);

}